package com.example.learneasy;

public class WordData{
    public String word;
    public String definition;
    public String example;
    public String usage;
    public String date;

    public WordData(String word, String definition, String example, String usage, String date) {
        this.word = word;
        this.definition = definition;
        this.example = example;
        this.usage = usage;
        this.date = date;
    }
}
